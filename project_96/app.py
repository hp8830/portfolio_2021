# Example 1: Demonstrating the islower() method:
print("JDJDJDdd".islower())
print('ahahahshshs'.islower())

# Example 2: Demonstrating the isspace() method:
print(' \n \n '.isspace())
print("   ".isspace())
print("mooo     \n".isspace())

# Example 3: Demonstrating the isupper() method:
print("MHHFFv".isupper())
print('mMMMMMM'.isupper())
print('HANNNAHHHH'.isupper())